function showsss() {
  var s = document.getElementById("signUp");
  s.style.transitionDuration = "1s";
  s.style.transform = "scale(1)";

  var f = document.getElementById("f");
  f.style.transitionDuration = "1s";
  f.style.transform = "scale(0)";
}

